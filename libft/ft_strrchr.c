#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int a;

	a = 0;
	while (s[a])
		a++;
	while (a != -1)
	{
		if (((char *)s)[a] == (char)c)
			return ((char *)s + a);
		a--;
	}
	return (0);
}
