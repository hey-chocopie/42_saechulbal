#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int a;

	a = 0;
	while (s[a])
	{
		if (((char *)s)[a] == (char)c)
			return ((char *)s + a);
		a++;
	}
	if ((char)c == 0)
		return ((char *)s + a);
	return (0);
}
