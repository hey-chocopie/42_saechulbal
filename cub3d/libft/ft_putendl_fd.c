#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int a;

	a = 0;
	if (fd < 0 || s == 0)
		return ;
	while (s[a])
	{
		write(fd, &(s[a]), 1);
		a++;
	}
	write(fd, "\n", 1);
}
